                marionnettes de ce show. J’ai donc très rapidement commencé le
                lourd process de design et fabrication de Jules Verne, son frère
                Paul, sa sœur Anna et de Dina un dinosaure. Un travail énorme
                pour une première en octobre et des répétitions début septembre.
                J’ai fait appel au génial Jean-Louis Veron pour fabriquer les
                mécanismes des marionnettes qui permettent d’ouvrir la bouche et
                de fermer les yeux. J’ai par la suite formé les
                comédiens-marionnettistes pendant plusieurs semaines pour qu’ils
                apprennent à jouer de ce nouvel instrument. J’ai ainsi passé des
                journées entières avec Emmanuel Suarez (qui était génial déjà
                sur Avenue Q), Cécilia Cara, Léo-Paul Salmain, Solen Sawen et
                toute la formidable troupe. Le spectacle, qui est reconduit pour
                une saison supplémentaire, est nommé aux Molières dans la
