                personnages fut un élément important car ils doivent jouer la
                comédie avec de vrais enfants sur le plateau. Il fallait donc
                une cohérence globale. Outre la création des personnages j’ai
                également eu la mission de leur donner vie pendant le tournage.
                Durant 3 semaines j’ai tourné 3 émissions par jours avec 9
                enfants au total. Il y avait un gros travail pour créer une
                dynamique avec les enfants et parfois les aider dans la
                direction. Ce fut une expérience passionnante et très ludique
                pour laquelle mes années de théâtre furent très utiles.
            </p>
